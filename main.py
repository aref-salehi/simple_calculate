
num1 = input ()
math_operations = input ()
num2 = input ()




if math_operations == ("+"):
    add(num1 , num2)
    
elif math_operations == ("-"):
    subtract(num1 , num2 )
    
elif math_operations == ("*"):
    multiply(num1 , num2 )
    
elif math_operations == ("/"):
    divide(num1 , num2 )
    
elif math_operations == ("%"):
    modulo(num1 , num2 )
    
elif math_operations == ("**"):
    power(num1 , num2 )
    
elif math_operations == ("\|"):
    square_root(num1 , num2 )
    
elif math_operations ==("!"):
    factorial(num1 )
    